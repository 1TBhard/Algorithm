/**
 * @param {number} n
 * @return {number}
 */
var climbStairs = function (n) {
	let arr = [0, 1, 1];

	for (var i = 0; i < n; i++) {
		arr[2] = arr[0] + arr[1];
		arr[0] = arr[1];
		arr[1] = arr[2];
	}

	return arr[2];
};

console.log(climbStairs(5));

